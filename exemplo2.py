# Operadores 
quantidade = 5
# > Verifica se o valor da quantidade é maior
# que 10
print(quantidade > 10)
# < Verifica se o valor da quantidade é menor
# que 10 
print(quantidade < 10) 
# == Verifica se o valor da quantidade é igual
# a 10
print(quantidade == 10) 
print(quantidade <= 10) # menor ou igual
print(quantidade >= 10) # maior ou igual
print(quantidade != 10) # diferente de

